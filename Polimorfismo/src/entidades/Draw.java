package entidades;

public class Draw {
	
	
	private int lados;
	
	private String nome;
	
	
	public Draw(int lados, String nome) {
		this.lados = lados;
		this.nome = nome;
	}

	public int getLados() {
		return lados;
	}

	public void setLados(int lados) {
		this.lados = lados;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	
	

}

